public class Dono {
	
	private String nome;

	/**
	 * @param string
	 */
	public Dono(String string) {
		this.nome = string;
	}

	/**
	 * @return the nome
	 */
	public String getNome() {
		return nome;
	}

	/**
	 * @param nome the nome to set
	 */
	public void setNome(String nome) {
		this.nome = nome;
	}

}
